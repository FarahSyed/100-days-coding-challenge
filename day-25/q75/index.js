var x = 4;
console.log("Initial value:", x);
// Addition Assignment
x += 4; // Means x = x + 4
console.log("Compound addition", x); // 4 + 4 = 8
// Subtraction Assignment
x -= 4; // Means x = x - 4
console.log("Compound subtraction", x); // 8 - 4 = 4
// Multiplication Assignment
x *= 4; // Means x = x * 4
console.log("Compound multiplication", x); // 4 * 4 = 16
// Division Assignment
x /= 4; // Means x = x / 4
console.log("Compound division", x); // 16 / 4 = 4
