// Initialize variable i with number 10
var i = 10;
// Logs the number and decrements it while the number is greater than or equal to 1
while (i >= 1) {
    // If i is strictly eqaul to 5, break the loop
    if (i === 5)
        break;
    console.log(i); // Log the current value of i to the console
    i--; // Decrement i
}
