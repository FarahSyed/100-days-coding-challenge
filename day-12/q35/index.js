var animals = ["Leopard", "Jaguar", "Tiger", "Cheetah"];
// Using for loop print a statement about each animal
for (var i = 0; i < animals.length; i++) {
    console.log("".concat(animals[i], " is a wild cat."));
}
// State what these animals have in common
console.log("Each of these wild cats can be a great pet!");
